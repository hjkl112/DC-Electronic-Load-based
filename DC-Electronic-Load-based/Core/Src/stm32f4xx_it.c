void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	if(GPIO_Pin==KEY_2_Pin)
	{
		if(HAL_GPIO_ReadPin(GPIOB,KEY_2_Pin)==0)
		{
			set_key=1;
		}
	}
	else if(GPIO_Pin==KEY_1_Pin)
	{
		if(HAL_GPIO_ReadPin(GPIOB,KEY_1_Pin)==0)
		{
			quit_key=1;
		}
	}		
}

